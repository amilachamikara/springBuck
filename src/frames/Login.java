    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String un = cmb_username.getSelectedItem().toString();
        String pw = String.valueOf(txt_password.getPassword());

        if (Validator.isValidUser(un, pw)) {
            if (chbx_remember.isSelected()) {
                try {
                    new JDBC().setData("UPDATE users SET remember=1 WHERE username='"+un+"'");
                } catch (Exception e) {
                    System.out.println(e);
                    
                }
            }else{
                try {
                    new JDBC().setData("UPDATE users SET remember=0 WHERE username='"+un+"'");
                } catch (Exception e) {
                    System.out.println(e);
                }
            }
            new MainPanel(un).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Password is incorrect!\nTry again!");
            txt_password.grabFocus();
        }

    }//GEN-LAST:event_btn_loginActionPerformed

