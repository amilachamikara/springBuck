    private void loadUsers() {
    
        cmb_username.removeAllItems();
        try {
            ResultSet rs = new JDBC().getData("SELECT username FROM users");
            while (rs.next()) {
                cmb_username.addItem(rs.getString("username"));
            }
        } catch (Exception e) {
        }
    }

    private void loadRemembers() {
        try {
            ResultSet rs = new JDBC().getData("SELECT * FROM users WHERE username = '"+cmb_username.getSelectedItem().toString()+"'");
            rs.next();
            if(rs.getInt("remember")==1){
                txt_password.setText(rs.getString("password"));
                chbx_remember.setSelected(true);
            }else{
                txt_password.setText(null);
                chbx_remember.setSelected(false);
            }
        } catch (Exception e) {
        }
    }
