    private void jXHyperlink1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jXHyperlink1ActionPerformed
        JFileChooser imageChooser = new JFileChooser();
        imageChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        imageChooser.setFileFilter(new FileNameExtensionFilter("Images", "bmp","jpg","png","jpeg"));
        path = getClass().getResource("../images/unknown.jpg").getPath();
        int reply = imageChooser.showOpenDialog(this);
        if(reply==JFileChooser.APPROVE_OPTION){
        path = imageChooser.getSelectedFile().getPath();
        imgbx_profile.setImage(new ImageIcon(path).getImage());
        }else{
            path = getClass().getResource("../images/unknown.jpg").getPath();
            imgbx_profile.setImage(new ImageIcon(path).getImage());        
        }
    }//GEN-LAST:event_jXHyperlink1ActionPerformed

