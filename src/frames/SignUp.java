    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txt_username.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Enter an username!");
            txt_username.grabFocus();
            
        }else if(txt_password.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Enter a password");
            txt_password.grabFocus();
        }else if(txt_repassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Re enter your password");
            txt_repassword.grabFocus();
        }else if(!txt_password.getText().equals(txt_repassword.getText())){
            JOptionPane.showMessageDialog(rootPane, "Passwords are not match! Check again!");
            txt_repassword.selectAll();
            txt_repassword.grabFocus();
        }else{
            
            User.createUser(txt_username.getText(), String.valueOf(txt_password.getPassword()),path.replace('\\', '$'));
            JOptionPane.showMessageDialog(rootPane, "New profile has been created!");
            clearFields();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

